-- eBay 1kg 이하 배송 국가 그룹 / YunExpress rate seed
-- Source workbook: YunExpress 운임 및 서비스가이드 MVPICK 2026.5
-- 기준 탭: 표준배송 (미국직항 일반화물)
-- 비교 탭: 표준운임
-- Rule: 100g~1000g 모든 bucket에서 standard_rate <= US direct baseline이면 baseline_1kg.

create table if not exists public.ebay_shipping_country_groups (
  group_code text primary key,
  name text not null,
  policy_mode text not null,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint ebay_shipping_country_groups_policy_mode_check
    check (policy_mode in ('baseline_included', 'surcharge', 'excluded'))
);

create table if not exists public.ebay_shipping_country_rates (
  country_code text not null,
  country_name text,
  group_code text not null references public.ebay_shipping_country_groups(group_code),
  weight_g integer not null,
  baseline_krw numeric not null,
  standard_krw numeric not null,
  delta_krw numeric not null,
  surcharge_usd numeric not null default 0,
  source_sheet text not null,
  valid_from date,
  created_at timestamptz not null default now(),
  primary key (country_code, weight_g),
  constraint ebay_shipping_country_rates_weight_1kg_check
    check (weight_g in (100,200,300,400,500,600,700,800,900,1000)),
  constraint ebay_shipping_country_rates_nonnegative_check
    check (baseline_krw >= 0 and standard_krw >= 0 and delta_krw >= 0 and surcharge_usd >= 0)
);

comment on table public.ebay_shipping_country_groups is
  'eBay shipping country groups for YunExpress 1kg-or-less phase-1 rollout.';
comment on table public.ebay_shipping_country_rates is
  'YunExpress 100g~1000g baseline vs standard rates for eBay margin and future rate-table surcharge work.';
comment on column public.ebay_shipping_country_rates.baseline_krw is
  '표준배송 (미국직항 일반화물) 기준 운임. 1kg 이하 eBay included-shipping baseline.';
comment on column public.ebay_shipping_country_rates.standard_krw is
  '표준운임 탭의 국가별 운임. US row mirrors baseline for domestic EBAY_US buyers.';
comment on column public.ebay_shipping_country_rates.delta_krw is
  'max(0, standard_krw - baseline_krw). Phase 2 surcharge source.';

